// создание списка
import java.util.ArrayList;

public class MinimalList {
    public static void main(String[] args) {
        // Создаем список
        ArrayList<String> fruits = new ArrayList<>();
        
        // Добавляем элементы
        fruits.add("Яблоко");
        fruits.add("Груша");
        fruits.add("Вишня");
        
        // Выводим
        System.out.println(fruits);
    }
}